/*
The switch statement is another control flow statement 
that can replace multiple if statements

*/

const day = 2;
switch (day) {
  case 1:
    console.log("Mon");
    break;
  case 2:
    console.log("Tue");
    break;
  case 3:
    console.log("Wed");
    break;
  case 4:
    console.log("Thur");
    break;
  case 5:
    console.log("Fri");
    break;
  default:
    console.log("Invalid number");
}
